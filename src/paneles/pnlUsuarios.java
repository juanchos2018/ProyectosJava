    public void  Listar()

    {
         String titulo[]={"codigo","Dni","Nombre","Apellidos","sexo","fechanaci","direccion","telefono","email","usuario","ESTado"};
   DefaultTableModel dtm=new DefaultTableModel(null,titulo);
        ClsNegUsuario Estudiante=new ClsNegUsuario();
        ArrayList<ClsEntUsuarios> Escu=Estudiante.ListarUsuario();
        Iterator iterator=Escu.iterator();
      
        
        String columna[]=new String[11];
        while (iterator.hasNext()) {
            ClsEntUsuarios Estu=new ClsEntUsuarios();
            Estu=(ClsEntUsuarios) iterator.next();
            columna[0]=String.valueOf(Estu.getCodigo_usu());
            columna[1]=Estu.getDni_usu();
            columna[2]=Estu.getNombre_usu();
             columna[3]=Estu.getApellidos_usu();
            columna[4]=Estu.getSexo_usu();
             columna[5]=Estu.getFechanaci_usu();
            columna[6]=Estu.getDireccion_usu();
              columna[7]=Estu.getTelefono_usu();
             columna[8]=Estu.getEmail_usu();
            columna[9]=Estu.getUsuario_usu();   
            columna[10]=Estu.getEstado_usu();  
            
                
            
               
     
            dtm.addRow(columna);
        }
        tblRegistro.setModel(dtm);   
        
    }
