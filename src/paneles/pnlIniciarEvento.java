    public void BusquedaEstudiante()
    {   int Busqueda;
        Busqueda= Integer.parseInt(txtcodigo.getText());        
        ClsNegEstudiante Estu = new ClsNegEstudiante();
        
        try{
            
            rs=Estu.ConsultarEstudiante2(Busqueda);
            boolean encuentra =false;
            while(rs.next()){
                txtnombres.setText((String) rs.getString(2));
                txtidescuela.setText((String)rs.getString(3));
                txtescuela.setText((String)rs.getString(4));
                encuentra= true;

            }
            if(encuentra== false){
                JOptionPane.showMessageDialog(null,"No existen registros","Tabla Consultas",1);
            }

        }catch(Exception ex){
            ex.printStackTrace();
        }
        
    
    }

public void BuscarSiEstalistaInvitados()
{
        int Busqueda;
        Busqueda= Integer.parseInt(txtcodigo.getText());    
        boolean encuentra =false; 
        ClsNegInvitados Escuela = new ClsNegInvitados();
        try{            
            rs=Escuela.ConsultarInvitados(idper[cboeventos.getSelectedIndex()],Busqueda);
           
            while(rs.next()){
             
                encuentra= true;
                BusquedaEstudiante();
                existe=true;
            }
            if(encuentra== false){
                   
              JOptionPane.showMessageDialog(null,"No estas en La Lista Wey");
               BusquedaEstudiante();
               existe=false;
                
            }

        }catch(Exception ex){
            ex.printStackTrace();
        }
        
}
