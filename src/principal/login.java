    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        // TODO add your handling code here:

        Usu = txtusuario.getText();
        Pass = txtclave.getText();

        //instanciar la clase usuario
        ClsNegUsuario usuario = new ClsNegUsuario();
        try {

            rs = usuario.LoginUsuario(Usu, Pass);
            boolean existe = false;

            while (rs.next()) {

                if (Usu.equals(rs.getString(10)) && Pass.equals(rs.getString(11))) {

                    JOptionPane.showMessageDialog(null, "Bienvenido " + rs.getString(3) + " " + rs.getString(4) + " al sistema");

                    //instanciar MDI
                    nom = rs.getString(3);
                    ape = rs.getString(4);
                    Principal frm = new Principal();
                    frm.setVisible(true);
                    dispose();

                }
                existe = true;
                break;

            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Lo sentimos..no exsite");
        }

    }//GEN-LAST:event_btnIngresarActionPerformed

